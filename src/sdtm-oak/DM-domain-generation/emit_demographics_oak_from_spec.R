         "    RFXSTDTC = NA_character_,",
         "    # End of participation date",
         "    RFXENDTC = NA_character_",
         "  )")
  L <- c(L, "")

  # Final variable selection - keep only SDTM variables
  L <- c(L, "  # Select final SDTM variables")
  final_vars <- c("STUDYID", "DOMAIN", "USUBJID", "SUBJID", "SITEID",
                  "AGE", "AGEU", "SEX", "RACE", "ETHNIC", "COUNTRY",
                  "DMDTC", "BRTHDTC", "DTHDTC", "DTHFL",
                  "RFSTDTC", "RFENDTC", "RFXSTDTC", "RFXENDTC", "RFICDTC", "RFPENDTC",
                  "ARM", "ARMCD", "ACTARM", "ACTARMCD",
                  "RANDDT", "RANDTC", "DMDTDIS", "DSTERM", "DSDECOD",
                  "INVID", "INVNAM", "DMDY")
  final_vars_str <- paste0("'", final_vars, "'", collapse = ", ")
  L <- c(L, sprintf("  dplyr::select(dplyr::any_of(c(%s)))", final_vars_str))
  L <- c(L, "")
  L <- c(L, "")
  L <- c(L, paste("#", paste(rep("-", 48), collapse = "")))
  L <- c(L, "# Save Results")
  L <- c(L, paste("#", paste(rep("-", 48), collapse = "")))
  L <- c(L, "")
  L <- c(L, "# Save as CSV")
  L <- c(L, sprintf("write.csv(%s, '%s_from_sdtm_oak_codegen.csv', row.names = FALSE)", dom, dom))
  L <- c(L, sprintf("cat('✓ SDTM %s dataset CSV created: %s_from_sdtm_oak_codegen.csv\\n')", dom, dom))
  L <- c(L, "")
  L <- c(L, "# Save as XPT format")
  L <- c(L, "if (requireNamespace('haven', quietly = TRUE)) {")
  L <- c(L, sprintf("  haven::write_xpt(%s, '%s_from_sdtm_oak_codegen.xpt')", dom, dom))
  L <- c(L, sprintf("  cat('✓ SDTM %s dataset XPT created: %s_from_sdtm_oak_codegen.xpt\\n')", dom, dom))
  L <- c(L, "} else {")
  L <- c(L, "  cat('⚠ haven package not available - XPT export skipped\\n')")
  L <- c(L, "  cat('  Install with: install.packages(\"haven\")\\n')")
  L <- c(L, "}")
  L <- c(L, "")
  L <- c(L, sprintf("cat('Dataset dimensions:', nrow(%s), 'rows x', ncol(%s), 'columns\\n')", dom, dom))

  # Write output file if specified
  if (!is.null(out_r_path)) {
    tryCatch({
      writeLines(L, out_r_path)
      cat("✓ Generated R code saved to:", out_r_path, "\n")
    }, error = function(e) {
      warning("Failed to write output file: ", e$message)
    })
  }

  # Final validation and summary
  cat("\n=== Code Generation Complete ===\n")
  cat("Generated code lines:", length(L), "\n")
  cat("Domain:", dom, "\n")
  cat("Subject count:", nrow(raw), "\n")
  cat("Timestamp:", format(Sys.time(), "%Y-%m-%d %H:%M:%S"), "\n")
  cat("=====================================\n\n")

  invisible(L)
}

